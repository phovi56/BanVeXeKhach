package com.example.dto;

public class ChuyenXeDTO {
	private String maCX;
	private String tenchuyen;
	private String noiDi;
	private String noiDen;
	private String ngayDi;
	private int khoangcach;
	private long maXe;
	
	public String getMaCX() {
		return maCX;
	}
	public void setMaCX(String maCX) {
		this.maCX = maCX;
	}
	public String getTenchuyen() {
		return tenchuyen;
	}
	public void setTenchuyen(String tenchuyen) {
		this.tenchuyen = tenchuyen;
	}
	public String getNoiDi() {
		return noiDi;
	}
	public void setNoiDi(String noiDi) {
		this.noiDi = noiDi;
	}
	public String getNoiDen() {
		return noiDen;
	}
	public void setNoiDen(String noiDen) {
		this.noiDen = noiDen;
	}
	public String getNgayDi() {
		return ngayDi;
	}
	public void setNgayDi(String ngayDi) {
		this.ngayDi = ngayDi;
	}
	public int getKhoangcach() {
		return khoangcach;
	}
	public void setKhoangcach(int khoangcach) {
		this.khoangcach = khoangcach;
	}
	public long getMaXe() {
		return maXe;
	}
	public void setMaXe(long maXe) {
		this.maXe = maXe;
	}
}
